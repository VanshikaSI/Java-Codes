class Car
{
    int id;
    String company;
    String color;
    int seats;

   void setId(int id)
   {
    this.id=id;
   } 
   int getId(){
    return this.id;
     }
    void  setCompany(String company)
   {
    this.company=company;
    }

    String getCompany(){
        return this.company;
    }
    void setColor(String color)
    {
        this.color=color;
    }
    String getColor()
    {
        return this.color;
            }
   void setSeats(int seats)
    {
        this.seats=seats;
    }
    int getSeats()
    {
        return this.seats;
    }
    


}